package com.ymedia.model;

public class MusicModel {

    private String type;
    private String musicName;
    private String singer;
    private String album;
    private String recommendation;
    private String hotOrder;
    private String publishTime;
    private String authority;
    private String internalOrder;
    private String flag;
    private String src;
    private String lrc;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getMusicName() {
        return musicName;
    }

    public void setMusicName(String musicName) {
        this.musicName = musicName;
    }

    public String getSinger() {
        return singer;
    }

    public void setSinger(String singer) {
        this.singer = singer;
    }

    public String getAlbum() {
        return album;
    }

    public void setAlbum(String album) {
        this.album = album;
    }

    public String getRecommendation() {
        return recommendation;
    }

    public void setRecommendation(String recommendation) {
        this.recommendation = recommendation;
    }

    public String getHotOrder() {
        return hotOrder;
    }

    public void setHotOrder(String hotOrder) {
        this.hotOrder = hotOrder;
    }

    public String getPublishTime() {
        return publishTime;
    }

    public void setPublishTime(String publishTime) {
        this.publishTime = publishTime;
    }

    public String getAuthority() {
        return authority;
    }

    public void setAuthority(String authority) {
        this.authority = authority;
    }

    public String getInternalOrder() {
        return internalOrder;
    }

    public void setInternalOrder(String internalOrder) {
        this.internalOrder = internalOrder;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }

    public String getSrc() { return src; }

    public void setSrc(String src) { this.src = src; }

    public String getLrc() { return lrc; }

    public void setLrc(String lrc) { this.lrc = lrc; }
}
